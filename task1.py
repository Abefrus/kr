def get_sbox_output(b: str, n: int) -> str:
    # Таблиця S_8
    S8 = [
        [13, 2, 8, 4, 6, 15, 11, 1, 10, 9, 3, 14, 5, 0, 12, 7],
        [1, 15, 13, 8, 10, 3, 7, 4, 12, 5, 6, 11, 0, 14, 9, 2],
        [7, 11, 4, 1, 9, 12, 14, 2, 0, 6, 10, 13, 15, 3, 5, 8],
        [2, 1, 14, 7, 4, 10, 8, 13, 15, 12, 9, 0, 3, 5, 6, 11]
    ]

    # Перевірка вхідних даних
    if len(b) != 6 or n != 8:
        raise ValueError("Бітовий рядок повинен мати довжину 6, а n повинно дорівнювати 8.")

    # Визначення рядка та стовпця
    row = int(b[0] + b[5], 2)  # Перший і останній біти
    col = int(b[1:5], 2)       # Середні 4 біти

    # Отримання значення з S8
    output_decimal = S8[row][col]

    # Перетворення у 4-бітний двійковий рядок
    output_binary = f"{output_decimal:04b}"

    return output_binary

# Вхідні дані
b = "111100"
n = 8

# Результат
result = get_sbox_output(b, n)
print(f"Результат виходу S-бокса: {result}")
